package day1_3;

public class RelOperator {
    
    public static void main(String args[]){
        int a=5, b=20, c=11;
        
        System.out.println(a>b); //false
        System.out.println(a<b); //true
        System.out.println(b>=c); //true
        System.out.println(c<=a); //false
        System.out.println(a==b); //false
        System.out.println(b!=c); //true
        
    }
    
}